package main.java.com.shera.sheraweight.model;

public class ProcessType {
    private String id;
    private String name;

    public ProcessType() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
